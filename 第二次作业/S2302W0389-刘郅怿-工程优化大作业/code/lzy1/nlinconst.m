        20.773 * X1 * X5 - 135.429 * X1 + 25.779 * X2^2 - ...
        15.08 * X2 * X4 + 8.781 * X2 * X5 - 123.145 * X2 + ...
        29.194 * X3^2 + 7.606 * X3 * X4 - 65.554 * X3 + ...
        31.565 * X4^2 - 15.874 * X4 * X5 - 93.243 * X4 - ...
        14.968 * X5^2 + 106.945 * X5 + 643.436 - 350;
        
        51.820 * X1 - 9.242 * X2 + 8.394 * X3 - ...
        79.998 * X4 - 64.932 * X5 - 5.156 * X1 * X2 + ...
        6.211 * X2 * X3 + 14.747 * X1 * X5 - 5.878 * X2 * X4 - ...
        9.894 * X2 * X5 - 8.811 * X3 * X4 - 2.477 * X3 * X5 + ...
        7.152 * X4 * X5 - 15.196 * X1^2 + 6.761 * X2^2 + ...
        20.438 * X4^2 + 7.471 * X5^2 + 275.327 - 200
    ];

    % 非线性等式约束
    ceq = []; 
end